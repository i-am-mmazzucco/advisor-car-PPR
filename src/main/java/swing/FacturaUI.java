    // Obtenemos el vendedor requerido.
    private void getVendedor(Hirachy hierachy) {
        try {
            ResultSet vendedor = hierachy.getVendedor(this.factura.getIdVendedor());
            while (vendedor.next()) {
                Integer id = vendedor.getInt("idVendedor");
                String nombre = vendedor.getString("nombre");
                String apellido = vendedor.getString("apellido");
                String dni = vendedor.getString("dni");
                String legajo = vendedor.getString("legajo");
                Vendedor vendedorInstance = new Vendedor(id, legajo, nombre, apellido, dni);
                this.vendedor = vendedorInstance;
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

    private void setLabels() {
        this.lblFecha.setText(this.factura.getFecha());
        this.lblVehiculo.setText(this.vehiculo.facturaStr());
        this.lblNombreCliente.setText(this.factura.getNombreCliente());
        this.lblApellidoCliente.setText(this.factura.getApellidoCliente());
        this.lblCluitCliente.setText(this.factura.getCuitcuilCliente());
        this.lblDireccionCliente.setText(this.factura.getDireccionCliente());
        this.lblDniCliente.setText(this.factura.getDniCliente());
        this.lblVendedor.setText(this.vendedor.facturaStr());
        this.lblMontoVenta.setText(this.factura.getMontoVenta().toString());
    }

